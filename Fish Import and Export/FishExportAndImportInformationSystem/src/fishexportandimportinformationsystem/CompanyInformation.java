    private void btnInsertExportFormActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertExportFormActionPerformed
       DBConnection db=new DBConnection();
       db.dbconnection();
       try{
            String country=(String)cmboxComapnyCountry.getSelectedItem(); 
            String sql="insert into Company values(?,?,?,?,?)";
            db.pst=db.con.prepareStatement(sql);
            db.pst.setString(1,txtCompanyId.getText());
            db.pst.setString(2,txtComapnyName.getText());
            db.pst.setString(3,txtCompanyContactNo.getText());
            db.pst.setString(4,country);
            db.pst.setString(5,txtAreaCompanyAddress.getText());
            
                String query="select * from Company";
                db.rs=db.st.executeQuery(query);
                while(db.rs.next()){
                String id=db.rs.getString("C_id");
                if(id.equals(txtCompanyId.getText()))
                {
                    JOptionPane.showMessageDialog(null,"ID is already exists!!!");
                    txtCompanyId.setText(" ");
                    break;
                }
            }
            
            if(txtCompanyId.getText().isEmpty() || txtComapnyName.getText().isEmpty() || txtCompanyContactNo.getText().isEmpty() || txtAreaCompanyAddress.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Please fill up all information");
            }
            else{
                db.pst.executeUpdate();
                close();
                JOptionPane.showMessageDialog(null,"Successful inserted");
                db.con.close();
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }                                                  
    }//GEN-LAST:event_btnInsertExportFormActionPerformed

    private void btnUpdateExportFormActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateExportFormActionPerformed
        DBConnection db=new DBConnection();
        db.dbconnection();
        try{
            String country=(String)cmboxComapnyCountry.getSelectedItem();
            String sql="update Company set C_name=?,C_contact_no=?,C_country=?,C_address=? where C_id=?";
            db.pst=db.con.prepareStatement(sql);
            
            db.pst.setString(1,txtComapnyName.getText());
            db.pst.setString(2,txtCompanyContactNo.getText());
            db.pst.setString(3,country);
            db.pst.setString(4,txtAreaCompanyAddress.getText());
            db.pst.setString(5,txtCompanyId.getText());
            if(txtCompanyId.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Please Insert only Company ID Number.");
            }
            else{
                db.pst.executeUpdate();
                close();
                JOptionPane.showMessageDialog(null,"Successfully Updated");
            }
            db.con.close();
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex);
            close();
        }
    }//GEN-LAST:event_btnUpdateExportFormActionPerformed

    private void btnDeleteExportFormActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteExportFormActionPerformed
        DBConnection db=new DBConnection();
        db.dbconnection();
        try{
            String sql="Delete from Company where C_id=?";
            db.pst=db.con.prepareStatement(sql);
            db.pst.setString(1,txtCompanyId.getText());
            if(txtCompanyId.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Please Insert only Company ID Number.");
            }
            else{
                db.pst.executeUpdate();
                close();
                JOptionPane.showMessageDialog(null,"Successfully Deleted.");
            }
            db.con.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Please Insert only Company ID Number.");
            close();
        }
    }//GEN-LAST:event_btnDeleteExportFormActionPerformed

